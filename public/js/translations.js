    ['hi', 'ar'].forEach((lang) => {
        if (!translations[lang]) translations[lang] = {};
        Object.keys(translations.en || {}).forEach((key) => {
            if (typeof translations[lang][key] === 'undefined') {
                translations[lang][key] = translations.en[key];
            }
        });
    });
