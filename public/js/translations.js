// Translation utility functions
const TranslationUtils = {
    // Normalize various language aliases to supported codes
    normalize(language) {
        const map = { hindu: 'hi', hindi: 'hi', hind: 'hi', arabi: 'ar', arabic: 'ar', arab: 'ar' };
        const v = (language || '').toString().trim().toLowerCase();
        if (!v) return 'en';
        if (v.startsWith('hi')) return 'hi';
        if (v.startsWith('ar')) return 'ar';
        return map[v] || v;
    },
    // Get current language
    getCurrentLanguage() {
        try {
            if (typeof localStorage !== 'undefined') {
                const stored = localStorage.getItem('appLanguage') || 'en';
                return this.normalize(stored) || 'en';
            }
        } catch (_) {}
        return 'en';
    },

    // Set current language
    setCurrentLanguage(language) {
        const normalized = this.normalize(language) || 'en';
        try { if (typeof localStorage !== 'undefined') localStorage.setItem('appLanguage', normalized); } catch(_) {}
        this.applyLanguageAttributes(normalized);
        if (typeof document !== 'undefined') {
            this.applyTranslations();
        }
    },

    // Apply HTML lang and text direction based on language
    applyLanguageAttributes(language = null) {
        const lang = language || this.getCurrentLanguage();
        const isRtl = (l) => ['ar', 'fa', 'he', 'ur'].includes(l);
        if (typeof document !== 'undefined') {
            document.documentElement.setAttribute('lang', lang);
            document.documentElement.setAttribute('dir', isRtl(lang) ? 'rtl' : 'ltr');
        }
    },

    // Get translation for a key
    get(key, language = null) {
        const currentLang = language || this.getCurrentLanguage();
        const langTranslations = translations[currentLang] || translations[this.normalize(currentLang)] || translations.en;
        
        return (langTranslations && langTranslations[key]) || translations.en[key] || key;
    },

    // Apply translations to all elements with data-translate attribute
    applyTranslations() {
        const currentLang = this.getCurrentLanguage();
        console.log('Applying translations for language:', currentLang);
        this.applyLanguageAttributes(currentLang);
        
        const elements = document.querySelectorAll('[data-translate]');
        console.log('Found', elements.length, 'elements with data-translate');
        
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.get(key, currentLang);
            
            console.log(`Translating ${key}: "${element.textContent}" -> "${translation}"`);
            
            if (translation) {
                element.textContent = translation;
            } else {
                console.warn(`No translation found for key: ${key}`);
            }
        });

        // Apply placeholder translations
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = this.get(key, currentLang);
            
            if (translation) {
                element.placeholder = translation;
            }
        });

        // Update language buttons
        document.querySelectorAll('.language-btn').forEach(btn => {
            const btnLang = btn.getAttribute('data-language');
            btn.classList.toggle('active', btnLang === currentLang);
        });
    },

    // Format translation with parameters
    format(key, ...params) {
        let translation = this.get(key);
        
        // Handle both indexed and named placeholders
        if (params.length === 1 && typeof params[0] === 'object') {
            // Named placeholders: format(key, {stars: 100, count: 2})
            const namedParams = params[0];
            Object.keys(namedParams).forEach(paramName => {
                translation = translation.replace(new RegExp(`{${paramName}}`, 'g'), namedParams[paramName]);
            });
        } else {
            // Indexed placeholders: format(key, param1, param2)
            params.forEach((param, index) => {
                translation = translation.replace(new RegExp(`{${index}}`, 'g'), param);
            });
        }
        
        return translation;
    },

    // Initialize translation system
    init() {
        this.applyTranslations();
        
        // Add event listeners for language switching
        document.querySelectorAll('.language-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                const language = btn.getAttribute('data-language');
                this.setCurrentLanguage(language);
            });
        });
    }
};

// Auto-initialize when DOM is loaded
if (typeof document !== 'undefined' && typeof window !== 'undefined') {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => {
            TranslationUtils.init();
        });
    } else {
        TranslationUtils.init();
    }
}

